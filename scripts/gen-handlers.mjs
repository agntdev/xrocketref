// Generate src/handlers.generated.ts — a STATIC import manifest of every feature
// handler in src/handlers/. The Cloudflare Workers runtime has no filesystem, so
// the Worker bundle can't readdirSync + dynamic-import handlers the way the Node
// entry does; this manifest gives esbuild concrete, statically-analysable imports
// to bundle. Run by `npm run build:worker` before esbuild. Node-only (build time).
import { readdirSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import path from "node:path";

const root = path.dirname(fileURLToPath(import.meta.url));
const handlersDir = path.join(root, "..", "src", "handlers");

let files = [];
try {
  files = readdirSync(handlersDir)
    .filter(
      (f) =>
        f.endsWith(".ts") &&
        !f.endsWith(".d.ts") &&
        !f.includes(".test.") &&
        !f.includes(".spec."),
    )
    .sort();
} catch (err) {
  if (err.code !== "ENOENT") throw err;
}

const imports = files
  .map((f, i) => `import h${i} from "./handlers/${f.replace(/\.ts$/, ".js")}";`)
  .join("\n");
const list = files.map((_, i) => `h${i}`).join(", ");

const out = `// AUTO-GENERATED by scripts/gen-handlers.mjs — do not edit by hand.
// Static handler manifest for the Cloudflare Workers bundle (no runtime fs).
import type { Composer } from "grammy";
import type { Ctx } from "./bot.js";
${imports}

// A handler may be typed Composer<Ctx> or a looser grammY Composer — a bare
// \`new Composer()\` infers Composer<Context>, which is NOT assignable to
// Composer<Ctx> even though the runtime is identical (the session middleware
// attaches ctx.session regardless). The Node entry disk-scans and casts handlers
// the same loose way, and the tokenless test harness gate never type-checks the
// Worker manifest — so failing the Worker build here on a purely compile-time
// strictness gap would reject bots that build and run fine on Node. Cast to the
// array type worker.ts consumes, matching the Node path's leniency.
export const handlers = [${list}] as unknown as Composer<Ctx>[];
`;

const target = path.join(root, "..", "src", "handlers.generated.ts");
writeFileSync(target, out);
console.log(`[gen-handlers] wrote ${files.length} handler(s) → src/handlers.generated.ts`);
